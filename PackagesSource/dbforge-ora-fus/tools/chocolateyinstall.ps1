  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url 
  softwareName  = 'Devart dbForge Fusion for Oracle, v3.9.12 Trial Edition' 
  checksum      = 'E4CA43FB85BE2162070169E619BE15DE135A4ABFDBD411E3CCA454411A263A24'
