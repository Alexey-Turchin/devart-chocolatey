  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url 
  softwareName  = 'dbForge SQL Complete, v6.2.37 Express Edition' 
  checksum      = 'FB11CEDCB60A9B0277B4F8E620517D427FB79D404458B1BB242B86E22C1BF104'
