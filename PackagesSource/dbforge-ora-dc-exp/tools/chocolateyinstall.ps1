  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'dbforge-ora-dc-exp' 
  checksum      = '11C618BF759FBB015BD9B1AE4CF49A76931033CFE95468C77EC4627B8FC3C25C'
  checksumType  = 'sha256' 
  silentArgs    = "/VERYSILENT"
  validExitCodes= @(0, 3010, 1641)
}
