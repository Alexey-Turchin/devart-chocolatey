$url        = 'https://www.devart.com/dbforge/mysql/studio/dbforgemysql80exp.exe' 
$url64      = '' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'dbForge Studio for MySQL, v8.0.40 Express Edition' 
  checksum      = '1E40FFE95BA4991B509A51CB9BAF5887F03717F3E84DCA29A119693EAD2DE8DA'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 