$url        = 'https://www.devart.com/dbforge/sql/monitor/monitorsql.exe' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url 
  softwareName  = 'dbForge Monitor for SQL Server, v1.2 ' 
  checksum      = 'F5E1C4717045B418441BD47E37D2614E7AEE03F0430C6EFC3BDBC83C65EFB450'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 