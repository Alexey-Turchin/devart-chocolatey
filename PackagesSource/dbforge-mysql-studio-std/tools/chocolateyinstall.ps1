  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'dbforge-mysql-studio-std' 
  checksum      = '84A5F50E77FFD09636B19270B18EB2ABA5ABDD76915788755F45AD12282C2216'
  checksumType  = 'sha256' 
  silentArgs    = "/VERYSILENT"
  validExitCodes= @(0, 3010, 1641)
}
