$url        = 'https://www.devart.com/dbforge/oracle/data-generator/datageneratororacle.exe' 
$url64      = '' 
$packageArgs = @{
  packageName   = $env:ChocolateyPackageName
  unzipLocation = $toolsDir
  fileType      = 'exe' 
  url           = $url
  url64bit      = $url64
  softwareName  = 'Devart dbForge Data Generator for Oracle, v1.0 Trial Edition' 
  checksum      = '9D107C60D7DB5A111FFA2D750C5C461363FC922346FFD7320457FCE611E5E9A6'
  checksumType  = 'sha256' 
  silentArgs    = '/VERYSILENT'
  validExitCodes= @(0, 3010, 1641)
}
Install-ChocolateyPackage @packageArgs 